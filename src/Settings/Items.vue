<template>
  <div>
    <div class='mdl-layout mdl-js-layout'>
      <div id='submit' class='mdl-card mdl-shadow--2dp'>
        <div class='mdl-card__supporting-text'>
          <form id='form'>
            <div class='mdl-textfield mdl-js-textfield'>
              <input id='name' v-model='name' class='mdl-textfield__input' type='text' required>
              <label class='mdl-textfield__label' for='name'>名前</label>
              <label class='mdl-textfield__error' for='name'>必須です</label>
            </div>
            <div class='mdl-textfield mdl-js-textfield'>
              <input id='price' class='mdl-textfield__input' type='number'>
              <label class='mdl-textfield__label' for='price'>値段</label>
            </div>
            <div class='mdl-textfield mdl-js-textfield'>
              <input id='count' class='mdl-textfield__input' type='number'>
              <label class='mdl-textfield__label' for='count'>個数</label>
            </div>
            <div class='mdl-textfield mdl-js-textfield'>
              <select id='category' class='mdl-textfield__input' name=''>
                <option value='' v-for=''></option>
              </select>
              <label class='mdl-textfield__label' for='category'>カテゴリー</label>
            </div>
            <div class='mdl-textfield mdl-js-textfield'>
              <select id='saller' class='mdl-textfield__input' name=''>
                <option value='' v-for=''></option>
              </select>
              <label class='mdl-textfield__label' for='saller'>仕入先</label>
            </div>
            <label id='preview' class='mdl-button mdl-js-button mdl-button--fab' for='image'>
              <i class='material-icons'>add_a_photo</i>
              <input id='image' class='mdl-textfield__input' type='file' accept='image/*' @change='preview_image'>
            </label>
          </form>
        </div>
        <div class='mdl-card__actions'>
          <button class='mdl-button mdl-js-button mdl-button--raised mdl-button--colored' @click='submit'>登録</button>
          <button v-if='has_item(name)' class='mdl-button mdl-js-button mdl-button--raised mdl-button--accent' @click='delete_item'>削除</button>
        </div>
      </div>
    </div>
    <div>
      <table class='mdl-data-table mdl-js-data-table mdl-data-table--selectable  mdl-shadow--2dp'>
        <thead>
          <tr>
            <th class='mdl-data-table__cell--non-numeric'></th>
            <th class='mdl-data-table__cell--non-numeric'>名前</th>
            <th>値段</th>
            <th>在庫数</th>
            <th class='mdl-data-table__cell--non-numeric'>仕入先</th>
          </tr>
        </thead>
        <tbody>
          <tr v-for='i in items' @click='set_name(i.name)'>
            <td class='mdl-data-table__cell--non-numeric'><img class='item_image' :src='i.image'></img></td>
            <td class='mdl-data-table__cell--non-numeric'>{{i.name}}</td>
            <td>{{i.price}}</td>
            <td>{{i.count}}</td>
            <td class='mdl-data-table__cell--non-numeric'>{{i.saller}}</td>
          </tr>
        </tbody>
      </table>
    </div>
  </div>
</template>

<script>
import path from 'path'
export default {
  data: function () { return { items: [], name: '' } },
  created: function () {
    let user = firebase.auth().currentUser
    let ref = firebase.firestore().collection(path.join('Zaiko', user.uid, 'items'))
    ref.onSnapshot((s) => {
      this.items.splice(0, this.items.length)
      s.forEach((d) => {
        this.items.push(d.data())
      })
    })
  },
  mounted: function () {
    componentHandler.upgradeDom()
  },
  methods: {
    preview_image: function (e) {
      let f = e.target.files[0]
      let r = new FileReader()
      r.onload = (e) => {
        document.getElementById('preview').style.backgroundImage = 'url(' + r.result + ')'
      }
      r.readAsDataURL(f)
    },
    submit: function (e) {
      e.target.disabled = true
      setTimeout(() => {
        let user = firebase.auth().currentUser
        // data set
        let name = document.getElementById('name')
        if (!name || name.value === '') {
          e.target.disabled = false
          name.focus()
          return
        }
        let price = document.getElementById('price')
        let count = document.getElementById('count')
        let saller = document.getElementById('saller')
        let data = {}
        data.name = name.value
        data.price = Number(price.value)
        data.count = Number(count.value)
        data.saller = saller.value
        data.time = new Date()
        const store = firebase.firestore()
        let collect = store.collection(path.join('Zaiko', user.uid, 'items'))
        let ref = collect.doc(name.value)
        ref.set(data).then(() => {
          this.clear_form()
        }).catch((err) => {
          console.log(err)
        })
        // file upload
        let file = document.getElementById('image').files[0]
        if (file) {
          let r = firebase.storage().ref()
          let fileRef = r.child(path.join(user.uid, name.value))
          fileRef.put(file).then(snapshot => {
            ref.set({image: snapshot.metadata.downloadURLs[0]}, {merge: true})
            document.getElementById('preview').style.backgroundImage = ''
            e.target.disabled = false
          }).catch(err => {
            console.log(err)
          })
        } else {
          e.target.disabled = false
        }
      }, 200, e)
    },
    delete_item: function () {
      let user = firebase.auth().currentUser
      let collect = firebase.firestore().collection(path.join('Zaiko', user.uid, 'items'))
      firebase.storage().ref().child(path.join(user.uid, this.name)).delete()
      collect.doc(this.name).delete()
    },
    clear_form: function () {
      this.name = ''
      document.getElementById('name').parentNode.MaterialTextfield.change('')
      document.getElementById('price').parentNode.MaterialTextfield.change('')
      document.getElementById('count').parentNode.MaterialTextfield.change('')
      document.getElementById('saller').parentNode.MaterialTextfield.change('')
      document.getElementById('image').value = null
    },
    has_item: function (item) {
      for (let i of this.items) {
        if (i.name === item) {
          return true
        }
      }
      return false
    },
    set_name: function (nameValue) {
      this.name = nameValue
      document.getElementById('name').parentNode.MaterialTextfield.change(this.name)
    }
  }
}
</script>

